    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
   
            try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/komsa_bpk", "root", "");

        // Insert into anggota table
        String insertAnggotaQuery = "INSERT INTO anggota VALUES ('"
                + nama_anggota.getText() + "', '"
                + jumlah_anggota.getText() + "', '"
                + alamat_anggota.getText() + "', '"
                + no_telp_anggota.getText() + "')";

        cn.createStatement().executeUpdate(insertAnggotaQuery);

        // Optionally, you may want to clear your text fields after successful insertion
        nama_anggota.setText("");
        jumlah_anggota.setText("");
        alamat_anggota.setText("");
        no_telp_anggota.setText("");

        // Refresh the table
        tampilkan();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error inserting data into anggota table: " + ex.getMessage());
    }

                    
    }//GEN-LAST:event_btnTambahActionPerformed

